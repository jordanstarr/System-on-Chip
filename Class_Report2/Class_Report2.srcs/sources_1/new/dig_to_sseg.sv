`timescale 1ns / 1ps

module dig_to_sseg(
    input logic [15:0] digit,
    
    output logic [7:0] ss0, 
    output logic [7:0] ss1,
    output logic [7:0] ss2,
    output logic [7:0] ss3
    );
    
    always_comb 
    begin 
        case (digit[3:0])
            0: ss0 = 8'b11000000; 
            1: ss0 = 8'b11111001; 
            2: ss0 = 8'b10100100; 
            3: ss0 = 8'b10110000; 
            4: ss0 = 8'b10011001; 
            5: ss0 = 8'b10010010; 
            6: ss0 = 8'b10000010; 
            7: ss0 = 8'b11111000; 
            8: ss0 = 8'b10000000; 
            9: ss0 = 8'b10010000; 
            10: ss0 = 8'b10001001; //H
            11: ss0 = 8'b11001111; //I
            15: ss0 = 8'b11111111; //blank
        endcase
        case (digit[7:4])
            0: ss1 = 8'b11000000; 
            1: ss1 = 8'b11111001; 
            2: ss1 = 8'b10100100; 
            3: ss1 = 8'b10110000; 
            4: ss1 = 8'b10011001; 
            5: ss1 = 8'b10010010; 
            6: ss1 = 8'b10000010; 
            7: ss1 = 8'b11111000; 
            8: ss1 = 8'b10000000; 
            9: ss1 = 8'b10010000; 
            10: ss1 = 8'b10001001; 
            11: ss1 = 8'b11001111;
            15: ss1 = 8'b11111111;
        endcase
        case (digit[11:8])
            0: ss2 = 8'b11000000; 
            1: ss2 = 8'b11111001; 
            2: ss2 = 8'b10100100; 
            3: ss2 = 8'b10110000; 
            4: ss2 = 8'b10011001; 
            5: ss2 = 8'b10010010; 
            6: ss2 = 8'b10000010; 
            7: ss2 = 8'b11111000; 
            8: ss2 = 8'b10000000; 
            9: ss2 = 8'b10010000; 
            10: ss2 = 8'b10001001; 
            11: ss2 = 8'b11001111;
            15: ss2 = 8'b11111111;
        endcase
        case (digit[15:12])
            0: ss3 = 8'b11000000; 
            1: ss3 = 8'b11111001; 
            2: ss3 = 8'b10100100; 
            3: ss3 = 8'b10110000; 
            4: ss3 = 8'b10011001; 
            5: ss3 = 8'b10010010; 
            6: ss3 = 8'b10000010; 
            7: ss3 = 8'b11111000; 
            8: ss3 = 8'b10000000; 
            9: ss3 = 8'b10010000; 
            10: ss3 = 8'b10001001; 
            11: ss3 = 8'b11001111;
            15: ss3 = 8'b11111111;
        endcase
    end 
    
endmodule
